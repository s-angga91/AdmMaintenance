    private void masuk() {
        try {
            String kd_user = new String(tKdUser.getPassword());
            int kd_usr = Integer.parseInt(kd_user);
            String pass = new String(tPswd.getPassword());
            s = c.createStatement();
            String sql = "Select * from user Where pswd='" + pass + "'And kd_user='" + kd_user + "'";
            r = s.executeQuery(sql);
            int baris = 0;
            while (r.next()) {
                baris = r.getRow();
            }
            if (baris == 1) {
                if (kd_usr == 100) {
                    tutupForm();
                    FormMenuManajer fmm = new FormMenuManajer();
                    fmm.setLocationRelativeTo(this);
                    fmm.setVisible(true);
                } else if (kd_usr == 200) {
                    tutupForm();
                    FormMenuAdmin fmkm = new FormMenuAdmin();
                    fmkm.setLocationRelativeTo(this);
                    fmkm.setVisible(true);
                } else if (kd_usr == 300) {
                    tutupForm();
                    FormMenuUser fma = new FormMenuUser();
                    fma.setLocationRelativeTo(this);
                    fma.setVisible(true);
                }
            } else {
                JOptionPane.showMessageDialog(null, "Login gagal, pastikan username dan password Anda benar!");
                BersihField();
                tKdUser.setEnabled(false);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "Username, password, atau kode user Anda salah !");
            tKdUser.setEnabled(false);
        }
    }

