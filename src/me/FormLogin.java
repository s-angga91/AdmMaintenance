    private void inputKode() {
        try {
            String nama = tUser.getText();
            String pass = new String(tPswd.getPassword());
            s = c.createStatement();
            String sql = "Select * from user Where username='" + nama + "'And pswd='" + pass + "' ";
            r = s.executeQuery(sql);
            if (r.next()) {
                tKdUser.setEnabled(true);
                tKdUser.requestFocus();
            } else {
                JOptionPane.showMessageDialog(null, "username & password Anda tidak ditemukan !", "Informasi", JOptionPane.INFORMATION_MESSAGE);
                BersihField();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "username dan password Anda tidak ditemukan ! ", "Informasi", JOptionPane.INFORMATION_MESSAGE);
        }
       }
