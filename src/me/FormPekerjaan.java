    private void Simpan() {
        String golPk = tKdPekerjaan.getText();
        try {
            PreparedStatement pStatement = c.prepareStatement("INSERT INTO jenis_pekerjaan"
                    + "(kd_pekerjaan,gol_pekerjaan"
                    + ")" + "VALUES (?,?)");
            pStatement.setString(1, tKdPekerjaan.getText());
            pStatement.setString(2, tGolPekerjaan.getText());
            if (pStatement.executeUpdate() > 0) {
                JOptionPane.showMessageDialog(this,
                        "Penambahan sukses", "Informasi",
                        JOptionPane.INFORMATION_MESSAGE);
                BersihField();
                tampilTabel();
            } else {
                JOptionPane.showMessageDialog(this,
                        "Penambahan gagal", "Informasi",
                        JOptionPane.INFORMATION_MESSAGE);
            }
            pStatement.close();
            c.close();
        } catch (SQLException e) {
            System.out.println("koneksi gagal " + e.toString());
            JOptionPane.showMessageDialog(null, "Gagal menyimpan \nGolongan Pekerjaan kembar :" + "\nKode Pekerjaan [ " + golPk + " ] telah ada, "
                    + "atau data yang Anda masukkan tidak sesuai format dengan yang diminta !");
            tKdPekerjaan.setFocusable(true);
        }
    }

    private void ubah() {
        String kdPk = tKdPekerjaan.getText();
        try {
            String message = "Apakah anda yakin akan mengubah data" + "\ndengan kode pekerjaan : " + kdPk + "?";
            int pesan = JOptionPane.showConfirmDialog(null, message, "Konfirmasi Ubah Data", JOptionPane.YES_NO_OPTION);
            if (pesan == 0) {
            PreparedStatement pStatement = c.prepareStatement("UPDATE jenis_pekerjaan SET gol_pekerjaan=? WHERE kd_pekerjaan=?");
            pStatement.setString(1, tGolPekerjaan.getText());
            pStatement.setString(2, tKdPekerjaan.getText());
            if (pStatement.executeUpdate() > 0) {
                JOptionPane.showMessageDialog(null, "Edit sukses", "Informasi", JOptionPane.INFORMATION_MESSAGE);
                BersihField();
            } else {
                JOptionPane.showMessageDialog(null, "Edit gagal", "Informasi", JOptionPane.INFORMATION_MESSAGE);
            }
            pStatement.close();
            textFalse();
            tombolFalse();
            tampilTabel();
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "koneksi gagal, atau data yang Anda masukkan tidak sesuai dengan yang diminta !", "Informasi", JOptionPane.INFORMATION_MESSAGE);
        }
    }

    private void hapus() {
        String kdPk = tKdPekerjaan.getText();
        try {
            String message = "Apakah anda yakin akan menghapus semua data" + "\ndengan kode pekerjaan : " + kdPk + "?";
            int pesan = JOptionPane.showConfirmDialog(null, message, "Konfirmasi Hapus Data", JOptionPane.YES_NO_OPTION);
            if (pesan == 0) {
                PreparedStatement pStatement = c.prepareStatement("DELETE FROM jenis_pekerjaan WHERE kd_pekerjaan=?");
                pStatement.setString(1, tKdPekerjaan.getText());
                if (pStatement.executeUpdate() > 0) {
                    JOptionPane.showMessageDialog(this,
                            "berhasil menghapus", "Informasi",
                            JOptionPane.INFORMATION_MESSAGE);
                    BersihField();
                } else {
                    JOptionPane.showMessageDialog(this,
                            "hapus gagal", "Informasi",
                            JOptionPane.INFORMATION_MESSAGE);
                }
                pStatement.close();
                c.close();
                BersihField();
                tampilTabel();
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "koneksi gagal " + e.toString());
        }
    }

    private void cari() {
        String param = tCari.getText();
        try {
            String kolom;
            if (cbCari.getSelectedItem().equals("Kode Pekerjaan")) {
                kolom = "kd_pekerjaan";
            } else {
                kolom = "";
            }
            Statement s = c.createStatement();
            String sql = "Select * from jenis_pekerjaan Where " + kolom + " ='" + param + "'";
            r = s.executeQuery(sql);
            if (r.next()) {
                textTrue();
                bUbah.setEnabled(true);
                bHapus.setEnabled(true);
                tKdPekerjaan.setText(r.getString("kd_pekerjaan"));
                tGolPekerjaan.setText(r.getString("gol_pekerjaan"));
            } else {
                JOptionPane.showMessageDialog(null, "Parameter tidak ditemukan !", "Informasi", JOptionPane.INFORMATION_MESSAGE);
                bHapus.setEnabled(true);
                bUbah.setEnabled(false);
                BersihField();
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Kesalahan pencarian ");
        }
    }
