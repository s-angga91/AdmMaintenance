    private void cari() {
        String param = tCari.getText();
        try {
            String kolom;
            if (cbCari.getSelectedItem().equals("Kode Service")) {
                kolom = "kd_service";
            } else {
                kolom = "";
            }
            Statement s = c.createStatement();
            String sql = "SELECT service_mesin.kd_service, service_mesin.mesin_id, mesin.nm_mesin, service_mesin.nik, pic.nm_pic, "
                    + "service_mesin.kd_pekerjaan, jenis_pekerjaan.gol_pekerjaan, service_mesin.mesin_stop, service_mesin.uraian_problem, service_mesin.penyebab, "
                    + "service_mesin.tindakan, service_mesin.jam_mulai, service_mesin.jam_selesai, service_mesin.durasi,"
                    + "service_mesin.tgl_pengajuan, service_mesin.diajukan_oleh, service_mesin.departement, service_mesin.shift "
                    + "FROM service_mesin, mesin, pic, jenis_pekerjaan WHERE service_mesin.mesin_id = mesin.mesin_id "
                    + "AND service_mesin.nik=pic.nik AND service_mesin.kd_pekerjaan = jenis_pekerjaan.kd_pekerjaan AND service_mesin." + kolom +  " = '"+ param +"'";
           r = s.executeQuery(sql);
            if (r.next()) {
                textServiceTrue();
                bUbah.setEnabled(true);
                bHapus.setEnabled(true);
                tKdService.setText(r.getString("service_mesin.kd_service"));
                String msnId = r.getString("service_mesin.mesin_id");
                String MsnId = r.getString("mesin.nm_mesin");
                String Mesin_id = msnId+"-"+MsnId;
                cbMesinId.setSelectedItem(Mesin_id);
                String nik = r.getString("service_mesin.nik");
                String nIk = r.getString("pic.nm_pic");
                String Nik = nik+"-"+nIk;
                cbNmPic.setSelectedItem(Nik);
                String pkrjaan = r.getString("service_mesin.kd_pekerjaan");
                String pKrjaan = r.getString("jenis_pekerjaan.gol_pekerjaan");
                String Pkrjaan = pkrjaan+"-"+pKrjaan;
                cbKdPekerjaan.setSelectedItem(Pkrjaan);
                tMesinStop.setText(r.getString("service_mesin.mesin_stop"));
                tUraianProblem.setText(r.getString("service_mesin.uraian_problem"));
                tPenyebab.setText(r.getString("service_mesin.penyebab"));
                tTindakan.setText(r.getString("service_mesin.tindakan"));
                String jMl = r.getString("service_mesin.jam_mulai");
                int i1 = jMl.length();
                String vBv = jMl.substring(0, 2);
                tJamMulai1.setText(vBv);
                tJamMulai1.setEnabled(false);
                int i = i1 - 2;
                String bvb = jMl.substring(i, i1);
                tJamMulai2.setText(bvb);
                tJamMulai2.setEnabled(false);
                String jSl = r.getString("service_mesin.jam_selesai");
                int k1 = jSl.length();
                String kmk = jSl.substring(0, 2);
                tJamSelesai1.setText(kmk);
                tJamSelesai1.setEnabled(false);
                int k = k1 - 2;
                String mkm = jSl.substring(k, k1);
                tJamSelesai2.setText(mkm);
                tJamSelesai2.setEnabled(false);
                String drs = r.getString("service_mesin.durasi");
                int ii = drs.length();
                String mnt = drs.substring(0, ii - 6);
                tDurasi.setText(mnt);
                tDurasi.setEnabled(false);
                tTglAju.setText(r.getString("service_mesin.tgl_pengajuan"));
                tNmPengaju.setText(r.getString("service_mesin.diajukan_oleh"));
                tDepartement.setText(r.getString("service_mesin.departement"));
                cbShift.setSelectedItem(r.getString("service_mesin.shift"));
            } else {
                JOptionPane.showMessageDialog(null, "Parameter tidak ditemukan ", "Informasi", JOptionPane.INFORMATION_MESSAGE);
                bHapus.setEnabled(true);
                bUbah.setEnabled(false);
                BersihField();
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Kesalahan pencarian ");
        }
    }
