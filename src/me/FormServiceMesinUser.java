    private void Simpan() {
        String kdservice = tKdService.getText();
        try {
            PreparedStatement pStatement = c.prepareStatement("INSERT INTO service_mesin"
                    + "(kd_service,mesin_id,nik,kd_pekerjaan,mesin_stop,uraian_problem,"
                    + "penyebab,tindakan,jam_mulai,jam_selesai,durasi,tgl_pengajuan,diajukan_oleh,departement,shift)"
                    + "VALUES (?,?,?,?,?,?,?,?,?,?,?,?,?,?,?)");
            pStatement.setString(1, tKdService.getText());
            String p1 = cbMesinId.getSelectedItem().toString();
            String p2 = p1.substring(0, 7);
            pStatement.setString(2, p2);
            String param1 = cbNmPic.getSelectedItem().toString();
            String param2 = param1.substring(0, 8);
            pStatement.setString(3, param2);
            String params1 = cbKdPekerjaan.getSelectedItem().toString();
            String params2 = params1.substring(0, 5);
            pStatement.setString(4, params2);
            pStatement.setString(5, tMesinStop.getText());
            pStatement.setString(6, tUraianProblem.getText());
            pStatement.setString(7, tPenyebab.getText());
            pStatement.setString(8, tTindakan.getText());
            String jmM1 = tJamMulai1.getText();
            String jmM2 = tJamMulai2.getText();
            String jmMlai = jmM1 + "." + jmM2;
            pStatement.setString(9, jmMlai);
            String jmS1 = tJamSelesai1.getText();
            String jmS2 = tJamSelesai2.getText();
            String jmSls = jmS1 + "." + jmS2;
            pStatement.setString(10, jmSls);
            String dRS = tDurasi.getText();
            String Drs = dRS + " menit";
            pStatement.setString(11, Drs);
            pStatement.setString(12, tTglAju.getText());
            pStatement.setString(13, tNmPengaju.getText());
            pStatement.setString(14, tDepartement.getText());
            pStatement.setString(15, cbShift.getSelectedItem().toString());
            if (pStatement.executeUpdate() > 0) {
                JOptionPane.showMessageDialog(this,
                        "Penambahan sukses", "Informasi",
                        JOptionPane.INFORMATION_MESSAGE);
                BersihField();
            } else {
                JOptionPane.showMessageDialog(this,
                        "Penambahan gagal", "Informasi",
                        JOptionPane.INFORMATION_MESSAGE);
            }
            pStatement.close();
            c.close();
        } catch (SQLException e) {
            System.out.println("koneksi gagal " + e.toString());
            JOptionPane.showMessageDialog(null, "Gagal menyimpan \nKode Service kembar :" + "\nKode Service [ " + kdservice + " ] telah ada, "
                    + "atau data yang Anda masukkan tidak sesuai format dengan yang diminta !");
            tKdService.setFocusable(true);
        }
    }

