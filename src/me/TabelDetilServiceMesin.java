    private Object[][] getData() {

        String user = "root";
        String pwd = "";
        String host = "localhost";
        String db = "maintenance";

        Object[][] data1 = null;

        try {
            Class.forName("com.mysql.jdbc.Driver");
            String urlValue = "jdbc:mysql://" + host + "/"
                    + db + "?user=" + user
                    + "&password=" + pwd;

            c = DriverManager.getConnection(urlValue);

            Statement st = c.createStatement();
            r = st.executeQuery(
                    "SELECT kd_service,uraian_problem,penyebab,tindakan,jam_mulai,jam_selesai,durasi,tgl_pengajuan,diajukan_oleh,departement,shift "
                    + " FROM service_mesin");

            r.last();
            int rowCount = r.getRow();
            r.beforeFirst();

            data1 = new Object[rowCount][12];
            int no = -1, angka = 1;
            while (r.next()) {
                no = no + 1;
                data1[no][0] = angka + ".";
                data1[no][1] = r.getString("kd_service");
                data1[no][2] = r.getString("uraian_problem");
                data1[no][3] = r.getString("penyebab");
                data1[no][4] = r.getString("tindakan");
                data1[no][5] = r.getString("jam_mulai");
                data1[no][6] = r.getString("jam_selesai");
                data1[no][7] = r.getString("durasi");
                data1[no][8] = r.getString("tgl_pengajuan");
                data1[no][9] = r.getString("diajukan_oleh");
                data1[no][10] = r.getString("departement");
                data1[no][11] = r.getString("shift");
                angka = angka + 1;
            }

            st.close();
            c.close();

        } catch (ClassNotFoundException e) {
            System.out.println("jdbc.Driver tidak ditemukan");
        } catch (SQLException e) {
            System.out.println("koneksi gagal " + e.toString());

        }

        return data1;
    }

